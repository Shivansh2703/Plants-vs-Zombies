volatile int pixel_buffer_start; // global variable

int main(void)
{
    volatile int * pixel_ctrl_ptr = (int *)0xFF203020;
    /* Read location of the pixel buffer from the pixel buffer controller */
    pixel_buffer_start = *pixel_ctrl_ptr;
    /* set front pixel buffer to start of FPGA On-chip memory */
    *(pixel_ctrl_ptr + 1) = 0xC8000000; // first store the address in the 
                                        // back buffer
    /* now, swap the front/back buffers, to set the front buffer location */
    wait_for_vsync();
    /* initialize a pointer to the pixel buffer, used by drawing functions */
    pixel_buffer_start = *pixel_ctrl_ptr;
    clear_screen(); // pixel_buffer_start points to the pixel buffer
    /* set back pixel buffer to start of SDRAM memory */
    *(pixel_ctrl_ptr + 1) = 0xC0000000;
    pixel_buffer_start = *(pixel_ctrl_ptr + 1); // we draw on the back buffer
    clear_screen(); // pixel_buffer_start points to the pixel buffer

    // inital setup - done only once at the start
    clear_screen();
	
	int pea_loc_x = 73;
	int pea_loc_y = 22;
	bool pea_collided = false;
	int zombie_loc_x = 260;
	int zombie_loc_y = 20;
	bool zombie_dead = false;
    // game loop - put all animations inside
	int time = 0;
    while (1)
    {
        pixel_buffer_start = *(pixel_ctrl_ptr + 1); // new back buffer
		clear_screen();
		draw_grass();
        draw_peashooter_icon(0, 30);
        
		draw_img_without_background(60, 20, peashooter_map, 20, 20);
		
		if (!zombie_dead)
		draw_img_without_background(zombie_loc_x, zombie_loc_y, zombie_map, 12, 20);
		if (!pea_collided)
		draw_img_without_background(pea_loc_x, pea_loc_y, pea_map, 10, 10);
        
		
        wait_for_vsync(); // swap front and back buffers on VGA vertical sync
		pixel_buffer_start = *(pixel_ctrl_ptr + 1); // new back buffer
		
		//time+=3;
		pea_loc_x += 3;
		zombie_loc_x -= 3;
		
		if ((pea_loc_x - zombie_loc_x) > -3 && (pea_loc_x - zombie_loc_x) < 3)
			{
				pea_collided = true;
				zombie_dead = true;
			}
				
				
				
    } 
}

