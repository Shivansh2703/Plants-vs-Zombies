void plot_pixel(int x, int y, short int line_color)
{
    *(short int *)(pixel_buffer_start + (y << 10) + (x << 1)) = line_color;
}

void draw_line(int x0, int y0, int x1, int y1, int color){
	
	int y_step;
	bool is_steep = ABS(y1-y0) > ABS(x1 - x0);
	if(is_steep){
		swap(&x0, &y0);
		swap(&x1, &y1);
	}
	if(x0 > x1){
		swap(&x0, &x1);
		swap(&y0, &y1);
	}
	
	int deltax = x1 - x0;
	int deltay = ABS(y1 - y0);
	int error = -(deltax / 2);
	int y = y0;
	if(y0 < y1){
		y_step = 1;
	}
	else{
		y_step = -1;
	}
	
	for(int x = x0 ; x < x1 ; x++){
		if(is_steep){
			plot_pixel(y, x, color);
		}
		else{
			plot_pixel(x, y, color);
		}
		error = error + deltay;
		if(error > 0){
			y = y + y_step;
			error = error - deltax;
		}
	}
}

void swap(int *x, int *y){
	int temp = *x;
	*x = *y; 
	*y = temp;
}

void wait_for_vsync()
{
	volatile int * pixel_ctrl_ptr = (int *)0xFF203020;
	register int status;
	
	*pixel_ctrl_ptr = 1;
	
	status = *(pixel_ctrl_ptr + 3);
	while ((status & 0x01) != 0)
	{
		status = *(pixel_ctrl_ptr + 3);
	}
}

void clear_screen(){
	for(int y = 0 ; y < RESOLUTION_Y ; y++){
		for(int x = 0 ; x < RESOLUTION_X ; x++){
			plot_pixel(x, y, 0x0);
		}
	}	
}

void draw_img(int x_loc, int y_loc, const uint8_t img[]) {
	int z = 0;
	for(int y = y_loc; y < y_loc + 20; y++){
		for(int x = x_loc; x < x_loc + 20; x++){
			plot_pixel(x, y, img[z+1] << 8 | img[z]);
			z+=2;
		}
	}
}

void draw_img_without_background(int x_loc, int y_loc, const uint8_t img[], int x_len, int y_len) {
	int z = 0;
	for(int y = y_loc; y < y_loc + y_len; y++){
		for(int x = x_loc; x < x_loc + x_len; x++){
			short int colour = img[z+1] << 8 | img[z];
			if(img[z+1] != 0xff || img[z] != 0xff){
				plot_pixel(x, y, colour);
			}
			z+=2;
		}
	}
}

